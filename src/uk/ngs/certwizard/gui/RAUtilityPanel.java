    private void btnCertSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCertSearchActionPerformed
        // TODO add your handling code here:
        if( SystemStatus.ISONLINE ){

            WaitDialog.showDialog();

            String _name = this.searchName.getText();
            String _email = this.searchEmail.getText();
            String _status = this.searchStatus.getSelectedItem().toString();
            String _ra = this.searchRA.getSelectedItem().toString();

            DefaultTableModel tModel = new DefaultTableModel(
                    new Object [][] {
                        {null, null, null, null, null, null, null} },
                        new String [] {"Serial", "CN", "OU", "L", "Status", "App. Date", "Exp. Date"} );
            this.jTable1.setModel(tModel);

            if( _name != null ){
                _name = _name.trim();
                if( _name.equals("") ){
                    _name = null;
                }
            }
            if( _email != null ){
                _email = _email.trim();
                if( _email.equals("") ){
                    _email = null;
                }
            }

            String encodedPublicKey = EncryptUtil.getEncodedPublicKey(this.publicKey);

            RASearch raSearch = new RASearch(this.privateKey, encodedPublicKey, _name, _ra, _email, _status, null);
            if( raSearch.doGet() ){
                RASearchInfo[] infos = raSearch.getRASearchInfos();
                int index = infos.length;

                for ( int i = 0; i < index; i++ ){
                    String serialNumber = infos[ i ].getSerialNumber();
                    String cn = infos[ i ].getCN();
                    String ou = infos[ i ].getOU();
                    String l = infos[ i ].getL();
                    String status = infos[ i ].getStatus();
                    String startdate = infos[ i ].getStartDate();
                    String enddate = infos[ i ].getEndDate();
                    tModel.addRow(new Object[] {serialNumber, cn, ou, l, status, startdate, enddate} );
                }
            }else{
               JOptionPane.showMessageDialog(this, "You fail to search certificate!", "Unsuccess Search", JOptionPane.INFORMATION_MESSAGE);
            }
          
            this.radarIcon.setVisible(false);

            WaitDialog.hideDialog();
        }
 
    }//GEN-LAST:event_btnCertSearchActionPerformed

