    public RAUtilityPanel( CertificateCSRInfo info ){
        this.certificateCSRInfo = info;

        initComponents();

        if (SystemStatus.ISONLINE) {
            String id = info.getId();
            long long_id = new Long( id ).longValue();

            String _pswdProperty = SysProperty.getValue("uk.ngs.ca.passphrase.property");
            String _pswd = System.getProperty(_pswdProperty);
            ClientKeyStore clientKeyStore = new ClientKeyStore( _pswd.toCharArray() );
            CertificateDownload certDownload = new CertificateDownload( id );
            X509Certificate _cert = certDownload.getCertificate();
            PublicKey _publicKey = _cert.getPublicKey();
            PrivateKey _privKey = clientKeyStore.getPrivateKey(_publicKey);
            this.privateKey = _privKey;
            this.publicKey = _publicKey;

            CAInfo caInfo = new CAInfo();
            String[] _ras = caInfo.getRAs();
            int _index = _ras.length + 1;
            this.RAs = new String[ _index ];
            this.RAs[ 0 ] = "ALL";
            for( int i = 0; i < _ras.length; i++ ){
                this.RAs[ i + 1 ] = _ras[ i ];
            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(RAs);
            this.searchRA.setModel(m);
            this.contactRA.setModel(m);
            m = new DefaultComboBoxModel(this.searchStatusArray);
            this.searchStatus.setModel(m);

            String _ou = _retrieveDataFromDN(this.certificateCSRInfo.getOwner(), "OU=");
            String _l = _retrieveDataFromDN(this.certificateCSRInfo.getOwner(), "L=");
            String _ra = _ou + " " + _l;
            this.searchRA.setSelectedItem(_ra);
            this.contactRA.setSelectedItem(_ra);

            CAMotd motd = new CAMotd();
            this.MotD = motd.getText();
            setMOD(this.MotD);


            this.radarIcon.setVisible(false);

        }

    }

    private String _retrieveDataFromDN(String dn, String data) {
        int index = dn.indexOf(data);
        index = index + data.length();
        String result = dn.substring(index);
        int _index = result.indexOf(",");
        if (_index != -1) {
            result = result.substring(0, _index);
        }
        result = result.trim();

        return result;
    }


